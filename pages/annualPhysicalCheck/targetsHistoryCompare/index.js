let chosenLabelType = []
function chooseLabelType(checkbox) {
    if(checkbox.checked === true) {
        chosenLabelType.push(checkbox.id)
    } else {
        chosenLabelType = chosenLabelType.filter((item) => item !== checkbox.id)
    }
    let filterShowDataAll = [];
    let showData = data[chooseThirdNavigate]
    for(let i=0; i<showData.length; i++ ) {
        for(let j=0; j<chosenLabelType.length; j++) {
            if(showData[i].id === chosenLabelType[j])
                filterShowDataAll.push(showData[i])
        }
    }

    let contentListDiv = ``;
    filterShowDataAll = filterShowDataAll.length ? filterShowDataAll : showData;
    for (let i=0; i < filterShowDataAll.length; i++ ) {
        let tableData = filterShowDataAll[i].tableData;
        let tableHeader = tableData.header;
        let dataSource = tableData.dataSource;
        let tableHeaderColumnDiv = '';
        for( let i=0; i<tableHeader.length; i++ ) {
            tableHeaderColumnDiv = tableHeaderColumnDiv + ` <div class="table-header-column">${tableHeader[i].title}</div>`
        }
        let tableDataColumnDiv = '';
        for( let i=0; i<dataSource.length; i++ ) {
            tableDataColumnDiv = tableDataColumnDiv + `
            <div class="table-content">
                 <div class="table-content-column">${dataSource[i].year}</div>
                      <div class="table-content-column">${dataSource[i].labelValue}</div>
                      <div class="table-content-column">
                      <div class=${dataSource[i].level}>${dataSource[i].levelValue}</div>
                 </div>
            </div>
        `
        }

        contentListDiv = contentListDiv + `
    <div class="content-list-item">
         <div class="content-list-item-left">
                        <div class="content-list-item-first-box">
                            <div class="content-list-item-label">指标名称：</div>
                            <div class="content-list-item-highlight">${filterShowDataAll[i].pointName}</div>
                        </div>
                        <div class="content-list-item-second-box">
                            <div class="content-list-item-label">指标解释：</div>
                            <div class="content-list-item-description">${filterShowDataAll[i].pointDescription}</div>
                        </div>
         </div>
         <div class="content-list-item-right">
                        <!--         中间表格          -->
                        <div class="content-table-box">
                            <div class="table-header">
                               ${tableHeaderColumnDiv}
                            </div>
                            ${tableDataColumnDiv}
                        </div>
                    </div>
         <div class="content-list-item-chart ${filterShowDataAll[i].id}"></div>
    </div>
    `
        setTimeout(() => {
            showEcharts(`.${filterShowDataAll[i].id}`, options)
        }, 0)
    }
    document.querySelector('.content-main').innerHTML = contentListDiv;

}

