function operationIconChecked(e) {
    chosenLabelType = []
    let target;
    if(e) {
        target = e.currentTarget;
        chooseThirdNavigate = e.currentTarget.dataset.thirdnavigate
        // 变换侧边栏选中样式颜色
        const targetClassList = target.classList;
        operationList.forEach(operation => {
            const classList = operation.classList;
            if (classList.contains(operationCheckedClass)) {
                classList.remove(operationCheckedClass);
            }
        });
        const uniqueClass = targetClassList[1];
        const checkedClass = `${uniqueClass}-checked`;
        targetClassList.add(checkedClass);
        operationCheckedClass = checkedClass;
    } else {
        chooseThirdNavigate = 'ecology'
    }

    // 显示对应页面数据
    let showData = data[chooseThirdNavigate];
    let contentListDiv = ``;
    let labelList = ``;
    for (let i=0; i < showData.length; i++ ) {
        let tableData = showData[i].tableData;
        let tableHeader = tableData.header;
        let dataSource = tableData.dataSource;
        let tableHeaderColumnDiv = '';
        for( let i=0; i<tableHeader.length; i++ ) {
            tableHeaderColumnDiv = tableHeaderColumnDiv + ` <div class="table-header-column">${tableHeader[i].title}</div>`
        }
        let tableDataColumnDiv = '';
        for( let i=0; i<dataSource.length; i++ ) {
            tableDataColumnDiv = tableDataColumnDiv + `
            <div class="table-content">
                 <div class="table-content-column">${dataSource[i].year}</div>
                      <div class="table-content-column">${dataSource[i].labelValue}</div>
                      <div class="table-content-column">
                      <div class=${dataSource[i].level}>${dataSource[i].levelValue}</div>
                 </div>
            </div>
        `
        }

        contentListDiv = contentListDiv + `
    <div class="content-list-item">
         <div class="content-list-item-left">
                        <div class="content-list-item-first-box">
                            <div class="content-list-item-label">指标名称：</div>
                            <div class="content-list-item-highlight">${showData[i].pointName}</div>
                        </div>
                        <div class="content-list-item-second-box">
                            <div class="content-list-item-label">指标解释：</div>
                            <div class="content-list-item-description">${showData[i].pointDescription}</div>
                        </div>
         </div>
         <div class="content-list-item-right">
                        <!--         中间表格          -->
                        <div class="content-table-box">
                            <div class="table-header">
                               ${tableHeaderColumnDiv}
                            </div>
                            ${tableDataColumnDiv}
                        </div>
                    </div>
        <div class="content-list-item-chart ${showData[i].id}"></div>
    </div>
    `

        labelList = labelList + `
                <div class="target-checkbox">
                    <input type="checkbox" id=${showData[i].id} onclick="chooseLabelType(this)">
                    <label for=${showData[i].id}>${showData[i].pointName}</label>
                </div>
        `
    }
    document.querySelector('.content-main').innerHTML = contentListDiv;

    // 渲染右侧的指标列表
    const labelListDiv = `
         <div class="content-right-wrapper">
                <div class="label-list">指标列表</div>
                ${labelList}
            </div>
    `
    document.querySelector('.content-right').innerHTML = labelListDiv;
}


